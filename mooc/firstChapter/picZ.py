import turtle
turtle.setup(800,500,200,200)
turtle.pensize(30)
turtle.colormode(255)
turtle.pencolor(111,22,11)
turtle.left(135)
turtle.penup()
turtle.fd(100*(2**1/2))
turtle.pendown()
turtle.right(135)
turtle.fd(200)
turtle.right(135)
turtle.fd(2*100*(2**1/2))
turtle.seth(0)
turtle.fd(200)
turtle.done()